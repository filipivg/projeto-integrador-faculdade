    public void PreencherTabela(String Caminho) {
        //Cria uma Lista com as informações do Excel
        ArrayList<Veiculo> Lista = Controle_Excel_Veiculo.LerExcel(Caminho);
        
        //Classe para Modificar a tabela sem modificar diretamente na interface
        DefaultTableModel modelo = (DefaultTableModel) TableVeiculo.getModel();
        
        //Seta o Numero de linhas da coluna para evitar algum erro
        modelo.setNumRows(0);
        
        //Percorre a Lista e colocando informações na Tabela
        for (Veiculo veiculo : Lista) {
            modelo.addRow(new Object[]{
                veiculo.getIDVeiculo(),
                veiculo.getPlaca(),
                veiculo.getMarca(),
                veiculo.getModelo(),
                veiculo.getAnoFab(),
                veiculo.getEstadoDoVeiculoTexto()
            });
        }
    }

