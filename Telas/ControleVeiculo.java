    private void BntEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntEstadoActionPerformed
        //Muda o Estado do veiculo
        String Caminho = "Veiculo.xlsx";
        
        ArrayList<Veiculo> Lista = Controle_Excel_Veiculo.LerExcel(Caminho);
        //Recebe a Linha selecionada
        int linha = TableVeiculo.getSelectedRow();
        
        //Caso nenhuma linha esteja selecionada recebe o valor de -1
        if(linha == -1){
            JOptionPane.showMessageDialog(null, "Selecione um veículo!");
        return;
        }
        
        //Muda as Informações do Excel do veiculo que está selecionado
        Veiculo veiculo = Lista.get(linha);
        
        //novoEstado Recebe o Valor contrario do Valor Atual do isEstadoDoVeic
        //Caso seja "true" ele irá receber "false"
        //e ja seta o novoEstado
        boolean novoEstado = !veiculo.isEstadoDoVeic();
        veiculo.setEstadoDoVeic(novoEstado);
        
        //Classe para Modificar a tabela sem modificar diretamente na interface
        DefaultTableModel modelo = (DefaultTableModel) TableVeiculo.getModel();
        
        //Muda o valor da Linha selecionada e da Coluna 5 Colocando o Estado em Texto
        modelo.setValueAt(veiculo.getEstadoDoVeiculoTexto(), linha, 5);
        
        //e para Finalizar Atualiza o Arquivo
        Controle_Excel_Veiculo.Transf_Excel(Lista, Caminho);
        Controle_TXT_Veiculo.AtualizarTxtExcel(Lista);
        
        JOptionPane.showMessageDialog(null, "Estado alterado para: " + veiculo.getEstadoDoVeiculoTexto());
    }//GEN-LAST:event_BntEstadoActionPerformed

